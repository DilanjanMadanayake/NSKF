                <h4>Before 1868</h4>
                <br/>
                <img src="<?php  echo base_url("images/history1.jpg"); ?>" class="img-responsive" alt="about" align="center"/>
                <br/>
                <blockquote>
                    <p align="justify">
                        Karate can trace its roots back to the early combat systems of China and probably further back in martial arts history to ancient India. But there's nothing special about that. Human beings have disagreed and fought with each other since the dawn of time - stories of individual and group conflict appear in the oral and written records of every known civilization.
Japan's oldest known book, the 8th century 'Kojiki' meaning "record of ancient matters" refers to combat systems and the 'ways of war'. In particular it speaks about the warrior's admiration for the sword.
ut we can skip the early records because Japanese Karate came much later. It evolved separately to archery, swordsmanship, wrestling and other original forms. It has no roots in mainland Japan.
In the sixteenth century various conflicts gave rise to organized fighting with swords (which had already existed in Japan for more than a thousand years) spears, bows and arrows and grappling - collectively called Bu-jitsu. Later these 'techniques' were stylized and became known as Bu-gei (martial arts). At this time military dictators called Shogun (commanders of force) and hereditary nobles (Samurai) ruled. The Samurai were professional soldiers who lived by a code called Bushido (way of the warrior) a set of ethical and moral rules of loyalty for the shogun or emperor. Medieval knights lived by a similar code of honor - Chivalry.
Towards the end of the last feudal period (1603-1868) peace prevailed in mainland Japan and martial arts were practiced in traditional schools called Koryu. The only uniquely Japanese unarmed striking art around at this time was Jiu-jitsu. As yet there was no history of Karate - it was unheard of.

                    </p>
                </blockquote>
                <br/>
                <h4>1868 to 1922</h4>
                <br/>
                <img src="<?php  echo base_url("images/history2.jpg"); ?>" class="img-responsive" alt="about" align="center"/>
                <br/>
                <blockquote>
                    <p align="justify">
                        In 1868 the military system ended as Japan unified and opened its doors to the world. The ruling Samurai became peacetime governors and the martial arts retreated from the chaos of the battlefield to the relative safety of practice halls and fields. This practice became known as Budo(martial way) emphasizing the responsibility that comes with the use of deadly force to develop character as well as technique.
Regular people joined former Samurai to study the martial arts and the whole approach to Kei-ko (practice) became formalized. In 1899, starting with Judo (gentle way) and then Kendo (way of the sword) in 1927, national organizations emerged with rules so that 'players' could compete against each other in a test of skills. In 1911, because of its inbuilt discipline, both of these arts under the general term 'Budo' became a compulsory part of school education. This lasted until 1945 and was probably a factor in the incredibly fast rebuilding of Japan after WW2..
Meanwhile the people of Okinawa in the Ryukyu archipelago of southern Japan had for some time practiced a weaponless form of hand and foot fighting introduced by merchants who made trading trips to Southern China. This only needed the use of bare fists, hands, elbows, feet, and knees. It was known among other things as "China-Hand" and "Okinawa-Hand". These movements became organized into various schools (Ryu-ha) by masters who practiced for life, refining the techniques and passing their knowledge on - mostly through tight family lines.
Two main schools emerged from neighboring towns – Naha and Shuri. The suffix 'Te' was added to each, meaning 'hand', i.e. Naha-te and Shuri-te. From this tradition came a diminutive schoolteacher who would take things to a new level...
                    </p>
                </blockquote>
                <br/>
                <img src="<?php  echo base_url("images/history3.jpg"); ?>" class="img-responsive" alt="about" align="center"/>
                      <br/>
                <blockquote>
                    <p align="justify">
                      The most significant development in the history of Karate in modern times was the arrival of Okinawan scholar Funakoshi Gichin in mainland Japan early last century - 1922 to be exact. Funakoshi blended his skills with the philosophy of Budo to create Karate-do (empty hand way). He demonstrated his art widely to the general public. With the endorsement of the Government, he established Karate as a curricular subject within the University Education system.
As a result of his life's efforts, Funakoshi later became known as the Father of Japanese Karate. Conditions were now perfect for Karate to expand. The people of post feudal Japan took easily to the austere training methods and added a new military dimension to it. Over time, Funakoshi's core students introduced ideas of their own. Some remained loyal to the master's teachings and others developed new techniques, philosophies and distinct ways of teaching. This was the beginning of Karate styles.
Karate never looked back. World War 2 stalled progress for a time, but as peace returned so did the Japanese appetite for anything that preserved the bruised national spirit. After the war four major Japanese Karate styles emerged - Funakoshi's Shotokan (name taken from his 'pen' name) and 3 others Wado, Shito and Goju. These together with other uniquely Okinawan Karate styles, which had continued to develop and diversify, became the springboard for Japanese Karate to make a big leap forward... the biggest leap in the story of Karate...   
                    </p>
                </blockquote>
