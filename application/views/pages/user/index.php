                <blockquote>
                    <p align="justify">
                        You don't need to know the full history of Karate leave that to the buffs. You need to understand the evolution of the eastern fighting arts no more than Picasso needed to know about Neanderthal cave art. The history of Karate is mildly interesting - but unnecessary.
A step by step account of what happened centuries ago between feuding warlords will do nothing for your progress - neither will legends of weaponless peasants defending themselves against armed attackers. Yet judging by the amount of stuff cut-and-pasted across the net, you would think knowledge of martial arts history was mandatory for all Karateka.
There will come a point in your Karate journey though, where curiosity will lead you to the historical record. When this happens turn up your BS filter because what you read was likely written a long time after it happened - by people who were not there!
Separating myth from fact is tricky but beyond the speculation you will find first hand material like the autobiography of Gichin Funakoshi the Okinawan who introduced Karate to Japan "Karate-do My Way of Life" (Kodansha). Worth a look, but don't expect more than a good story.
We know that the seeds of modern day Karate came from China and were sown in the former Ryukyu kingdom - now the southern islands of Japan. Exactly how and when is unknown. If you want hard evidence, collections of documents and artifacts exist in places like the Okinawan Karate museum. The museum displays local and Chinese training implements and weaponry - it is probably the most significant living record of the history of Karate anywhere in the world - a 'must visit' for serious researchers.
                    </p>                    
                </blockquote> 
