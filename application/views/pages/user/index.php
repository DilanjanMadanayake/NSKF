                <h4>Gichin Funakoshi - Karate Master</h4>
                <br/>
                1868 - 1957. The Father of Modern Karate
                <br/>
                <img src="<?php  echo base_url("images/history4.jpg"); ?>" class="img-responsive" alt="about" align="center"/>
                <br/>
                <blockquote>
                    <p align="justify">
                        As much as we read about martial arts history, the history of Karate itself is not exactly 'ancient'. 150 years of reliable records can be divided roughly into 3 eras.
- Pre 1920 - 1920 to 1950 - 1950 onwards...
All three eras involve the diminutive Okinawan who became known as the Father of Karate-do, supreme master "Shoto" Gichin Funakoshi Sensei.
Funakoshi was such an influential figure that his teachings are claimed to be the foundations of many Karate styles.
The name Gichin Funakoshi means something to all traditional Karate styles. Whether he was influenced by the great teachers of the Goju tradition, whether he favored one University faction over another, or whether he approved of the course his students took toward the end of his life, one thing is clear; he is the major figure in the development of Japanese Karate. Image of Funakoshi Sensei Courtesy of Louis Sollune.
                </p>
                </blockquote>
